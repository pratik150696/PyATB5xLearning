# Take a three input from the user,
# Perform sum, sub, multi, div

num1 = int(input("Enter Number One "))
num2 = int(input("Enter Number Two "))
num3 = int(input("Enter Number Three "))

sum = num1 + num2 + num3
sub = num1 - num2 - num3
multi = num1 * num2 * num3
div = num1 / num2 / num3

print("Sum is: ", sum)
print("Sub is: ", sub)
print("Multi is: ", multi)
print("Div is: ", div)
